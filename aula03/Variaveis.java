package com.stefanini.aula03;

public class Variaveis {

	public static void main(String[] args) {
		byte numByte = 127; // de -128 a 127 (8bits)
		short numShort = -32768; // de -32768 a 32767 (16bits)
		int numInt = 2147483647; // de -2.147.483.648 a 2.147.483.648 (32bits)
		long numLong = 9223372036854775807L; // de -9.223.372.036.854.775.808 a 9.223.372.036.854.775.807 (64bits)

		float numFloat = 10.000f; // de 1.175494351 E - 38 a 3,402823466 E + 38 (32bits)
		double numDouble = 1000000000.00000; // de 	2.2250738585072014 E - 308 a 1,7976931348623158 E + 308 (64bits)
		
		char varChar = 'F'; // de 0 a 65535 (16bits)
		
		boolean varBoolean = true;  // true ou false (1bit)
		
		String texto = "Bem vindo Devs!";
		Integer numeroConta = 4000;
		Long numeroLongo = 12192818L;
	}

}
